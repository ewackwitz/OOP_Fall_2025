package edu.cvtc.java;

public class SquareNumbers {

  public static void main(String[] args) {

    System.out.println("Number  Number Squared");
    System.out.println("------------------------");

    for (int number = 0; number <= 20; number++) {
      System.out.println(number + "\t\t" + (number * number));
    }



  }

}
